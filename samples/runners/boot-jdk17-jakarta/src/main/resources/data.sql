-- ------------------------------------------------------------
-- TB_LARGE (1000 rows — non-canonical seed retained for paging/streaming demo)
-- HSQLDB 2.x PSM WHILE loop inside BEGIN ATOMIC.
-- ------------------------------------------------------------
CREATE PROCEDURE SEED_TB_LARGE()
MODIFIES SQL DATA
BEGIN ATOMIC
  DECLARE v_n INTEGER DEFAULT 1;
  WHILE v_n <= 1000 DO
    INSERT INTO TB_LARGE (NAME, STATUS)
    VALUES ('name' || CAST(v_n AS VARCHAR(10)), MOD(v_n, 3) + 1);
    SET v_n = v_n + 1;
  END WHILE;
END^^
CALL SEED_TB_LARGE()^^
DROP PROCEDURE SEED_TB_LARGE^^
